// 9.1 - Operadores Aritméticos (Matemáticos)
// Adicão = +, Subtração = -, Multiplicação =  *, Divisão = /, Exponencial = **
let salario = 10;

console.log("Adição = ", salario + salario);
console.log("Subtração = ", salario - salario);
console.log("Divisão = ", salario / salario);
console.log("Multiplicação = ", salario * salario);
console.log("Exponencial = ", salario ** salario);
// Incrementar = ++, Decrementar = --
let idade = 18;

console.log("Incrementar = ", ++idade);
// Ao colocar o Incrementar antes da variável ele soma e mostra
// Ao colocar o Incrementar depois da variável ele soma e depois mostra em outro console.log

let idade2 = 10;
console.log("Decrementar = ", --idade2);
// Ao colocar o Decrementar antes da variável ele soma e mostra
// Ao colocar o Decrementar depois da variável ele soma e depois mostra em outro console.log


